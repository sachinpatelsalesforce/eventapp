export function BottomNav() {
  const pathname = usePathname()
  const { isEnabled } = useSettings()

  if (pathname.startsWith('/admin')) return null

  const visibleItems = allNavItems.filter(item => item.feature === null || isEnabled(item.feature))

  return (
    <nav className="fixed bottom-0 inset-x-0 bg-white border-t border-gray-200 z-50 md:hidden">
      <div className="flex justify-around items-center h-16 px-2 pb-safe">
        {visibleItems.map(item => {
          const active = item.href === '/' ? pathname === '/' : pathname.startsWith(item.href)
          return (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                'flex flex-col items-center gap-0.5 min-w-0 flex-1 py-1 rounded-xl transition-colors',
                active ? 'text-[#0070D2]' : 'text-gray-400 hover:text-gray-600'
              )}
            >
              {item.icon(active)}
              <span className="text-[10px] font-medium leading-none">{item.label}</span>
            </Link>
          )
        })}
      </div>
    </nav>
  )
}
